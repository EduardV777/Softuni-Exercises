function F(num,num2,txt){
  if(txt=="+"){
    console.log(eval(num+txt+num2))
  }else if(txt=="-"){
    console.log(eval(num+txt+num2))
  }else if(txt=="*"){
    console.log(eval(num+txt+num2))
  }else if(txt=="/"){
    console.log(eval(num+txt+num2))
  }else if(txt=="%"){
    console.log(eval(num+txt+num2))
  }else if(txt=="**"){
    console.log(eval(num+txt+num2))
  }
}
